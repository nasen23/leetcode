use std::cmp;

impl Solution {
    pub fn minimum_moves(grid: Vec<Vec<i32>>) -> i32 {
        // Don't know why but this does not pass the very last test case
        let (r, c) = (grid.len(), grid[0].len());
        if grid[r - 1][c - 1] == 1 || grid[r - 1][c - 2] == 1 {
            return -1;
        }
        // 0 for _, 1 for |
        macro_rules! n {
            ($x:expr) => {
                match $x {
                    0 => 1,
                    _ => 0,
                }
            };
        };
        let max = 0x3f3f3f3f;
        let mut dp = vec![vec![vec![max, max]; c + 1]; r + 1];
        dp[1][1][0] = 0;
        for i in 1..r + 1 {
            for j in 1..c + 1 {
                if grid[i - 1][j - 1] != 1 {
                    for k in 0..2 {
                        if i + k < r + 1 && j + n!(k) < c + 1 && grid[i + k - 1][j + n!(k) - 1] != 1
                        {
                            dp[i][j][k] = cmp::min(
                                dp[i][j][k],
                                1 + cmp::min(dp[i - 1][j][k], dp[i][j - 1][k]),
                            );
                            if i < r
                                && j < c
                                && grid[i][j] != 1
                                && grid[i + n!(k) - 1][j + k - 1] != 1
                            {
                                dp[i][j][k] = cmp::min(
                                    dp[i][j][k],
                                    2 + cmp::min(dp[i - 1][j][n!(k)], dp[i][j - 1][n!(k)]),
                                );
                            }
                        }
                    }
                }
            }
        }

        if dp[r][c - 1][0] >= max {
            -1
        } else {
            dp[r][c - 1][0]
        }
    }
}

